class GridWorldWrapper:
    """Small adapter around DiscreteGridWorldEnv.

    The gridworld uses discrete actions (0=up, 1=down, 2=left, 3=right) and
    returns observations as [x, y, steps]. This wrapper provides a consistent
    interface with action shape conversion for compatibility.
    """

    def __init__(self, grid_size=5, max_steps=50):
        self.env = DiscreteGridWorldEnv(grid_size=grid_size, max_steps=max_steps)

    def reset(self, seed=None):
        """Reset the environment and optionally seed it."""
        if seed is not None:
            self.env.seed(seed)
        return self.env.reset()

    def step(self, action):
        """Take one step in the gridworld.
        
        Args:
            action: discrete action as array or scalar (0=up, 1=down, 2=left, 3=right)
        
        Returns:
            (state, reward, terminated, truncated, info)
        """
        if isinstance(action, np.ndarray):
            action_val = int(action[0]) if action.size > 0 else 0
        else:
            action_val = int(action)
        return self.env.step(action_val)

    @property
    def action_space(self):
        return self.env.action_space

    @property
    def observation_space(self):
        return self.env.observation_space

    def close(self):
        return self.env.close()

    def seed(self, seed=None):
        return self.env.seed(seed)


