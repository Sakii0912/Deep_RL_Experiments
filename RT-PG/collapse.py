def set_seed(seed: int) -> None:
    """Make the run reproducible.

    This sets the Python, NumPy, and PyTorch RNGs, and asks PyTorch to prefer
    deterministic kernels where possible.
    """
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(seed)
    if hasattr(torch.backends, "cudnn"):
        torch.backends.cudnn.deterministic = True
        torch.backends.cudnn.benchmark = False
    try:
        torch.use_deterministic_algorithms(True, warn_only=True)
    except Exception:
        pass


def to_tensor(array, device=CFG.device, dtype=torch.float32):
    """Convert a NumPy array or Python sequence to a Torch tensor on CFG.device."""
    return torch.as_tensor(array, dtype=dtype, device=device)


def as_numpy(tensor):
    """Detach a tensor and move it back to CPU NumPy form when needed."""
    if isinstance(tensor, torch.Tensor):
        return tensor.detach().cpu().numpy()
    return np.asarray(tensor)


class MLP(nn.Module):
    """Plain multilayer perceptron used by the policy and the models."""

    def __init__(self, input_dim, output_dim, hidden_sizes, activation=nn.Tanh):
        super().__init__()
        layers = []
        last_dim = input_dim
        for hidden_dim in hidden_sizes:
            layers.append(nn.Linear(last_dim, hidden_dim))
            layers.append(activation())
            last_dim = hidden_dim
        layers.append(nn.Linear(last_dim, output_dim))
        self.net = nn.Sequential(*layers)

    def forward(self, x):
        return self.net(x)


class GaussianPolicy(nn.Module):
    """Stochastic Gaussian policy \pi_\theta(a_t | s_t).

    The policy predicts a mean action \mu_\theta(s_t) and uses a learned but
    clipped log-standard-deviation. Sampling is done from a diagonal Gaussian
    and then clamped to the action range [-1, 1].
    """

    def __init__(self, state_dim, hidden_sizes, action_dim=1):
        super().__init__()
        self.action_dim = action_dim
        self.mean_net = MLP(state_dim, action_dim, hidden_sizes, activation=nn.Tanh)
        self.log_std = nn.Parameter(torch.full((action_dim,), -0.5))

    def distribution(self, states):
        """Return the Gaussian parameters (mean, std) for a batch of states."""
        mean = self.mean_net(states)
        log_std = torch.clamp(self.log_std, -5.0, 2.0)
        std = torch.exp(log_std).expand_as(mean)
        return mean, std

    def sample_action(self, state, deterministic=False):
        """Sample a_t ~ \pi_\theta(\cdot | s_t) or return the mean action.

        Returns a tuple of:
        - clamped action used by the environment
        - the same clamped action again (kept for notebook compatibility)
        - log \pi_\theta(a_t | s_t) for the sampled raw action
        """
        state_tensor = to_tensor(state).view(1, -1)
        mean, std = self.distribution(state_tensor)
        if deterministic:
            raw_action = mean
        else:
            raw_action = mean + std * torch.randn_like(mean)
        log_prob = self.log_prob(state_tensor, raw_action)
        continuous_action = torch.clamp(raw_action, -1.0, 1.0)
        return continuous_action.squeeze(0), continuous_action.squeeze(0), log_prob.squeeze(0)

    def log_prob(self, states, actions):
        """Compute log \pi_\theta(a_t | s_t) for a batch of state-action pairs."""
        if states.dim() == 1:
            states = states.unsqueeze(0)
        if actions.dim() == 1:
            actions = actions.unsqueeze(0)
        mean, std = self.distribution(states)
        var = std.pow(2)
        log_scale = torch.log(std)
        return -0.5 * (((actions - mean) ** 2) / var + 2.0 * log_scale + math.log(2.0 * math.pi)).sum(dim=-1)

    def copy(self):
        """Deep-copy the policy parameters into a new module."""
        cloned = GaussianPolicy(
            state_dim=self.mean_net.net[0].in_features,
            hidden_sizes=tuple(layer.out_features for layer in self.mean_net.net if isinstance(layer, nn.Linear))[:-1],
            action_dim=self.action_dim,
        )
        cloned.load_state_dict(copy.deepcopy(self.state_dict()))
        cloned.to(next(self.parameters()).device)
        return cloned





