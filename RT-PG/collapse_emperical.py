class CartPoleWrapper:
    """Small adapter around ContinuousCartPoleEnv.

    The notebook works with a continuous 1D action and a custom physics-based
    CartPole implementation. This wrapper keeps the environment interface simple
    for the rest of the script while handling action shape conversion, reward
    calculation, and termination flags.
    """

    def __init__(self):
        self.env = ContinuousCartPoleEnv()

    def reset(self, seed=None):
        """Reset the environment and optionally seed it."""
        if seed is not None:
            self.env.seed(seed)
        return self.env.reset()

    def step(self, action):
        """Advance the physics by one time step using the provided action.

        The action is treated as the scalar control input a_t. Internally the
        environment expects a small array, so this method normalizes the input
        shape before calling the underlying physics update.
        """
        if isinstance(action, (int, float)):
            action = np.array([action], dtype=np.float32)
        elif isinstance(action, np.ndarray) and action.ndim == 0:
            action = np.array([float(action)], dtype=np.float32)

        assert self.env.action_space.contains(action), \
            "%r (%s) invalid" % (action, type(action))

        force = self.env.force_mag * float(action.item() if action.size == 1 else action[0])

        self.env.state = self.env.stepPhysics(force)
        x, x_dot, theta, theta_dot = self.env.state

        done = x < -self.env.x_threshold \
            or x > self.env.x_threshold \
            or theta < -self.env.theta_threshold_radians \
            or theta > self.env.theta_threshold_radians
        done = bool(done)

        if not done:
            reward = 1.0
        elif self.env.steps_beyond_done is None:
            self.env.steps_beyond_done = 0
            reward = 1.0
        else:
            if self.env.steps_beyond_done == 0:
                reward = 1.0
            self.env.steps_beyond_done += 1
            reward = 0.0

        return np.array(self.env.state, dtype=np.float32), reward, done, False, {}

    @property
    def action_space(self):
        return self.env.action_space

    @property
    def observation_space(self):
        return self.env.observation_space

    def close(self):
        return self.env.close()

    def seed(self, seed=None):
        return self.env.seed(seed)


